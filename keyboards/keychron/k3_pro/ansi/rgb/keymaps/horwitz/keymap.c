/*
 * sets the values in [layer_used_indices] to the indices (0-83, in increasing order) that are used in the given [layer]
 * ("used" = not KC_TRNS) and returns how many values were set. (e.g., if keys at index 0, 5, 83 are the only ones set,
 * layer_used_indices will start { 0, 5, 83, ... } (followed by 0s) and [initialize_layer_used_indices] will return 3.)
 *
 * why [offset]? rgb_matrix_set_color's first argument appears to be based on an index of keys _not_ including
 * KC_NO--there are 96 (=[MATRIX_ROWS] * [MATRIX_COLS]) keycodes in each keymaps[layer], but (apparently) the non-KC_NO
 * keys are indexed 0-83 (and there are 96-84=12 instances of KC_NO (per layer))... see LAYOUT_ansi_84's definition in
 * obj_keychron_k3_pro_ansi_rgb/src/default_keyboard.h
 */
 int initialize_layer_used_indices(int layer, char* layer_used_indices) {
    int lui_i = 0;
    int offset = 0;
    for (int i = 0; i < MATRIX_ROWS * MATRIX_COLS; ++i) {
        int col = i % MATRIX_COLS;
        int row = i / MATRIX_COLS;
        switch (keymaps[layer][row][col]) {
            case KC_TRNS:
                break;
            case KC_NO:
                ++offset;
                break;
            default:
                layer_used_indices[lui_i++] = i - offset;
                break;
        }
    }

    return lui_i;
}

char layers_used_indices[NUM_LAYERS][NUM_KEYS];
// TODO handle this differently(?) (e.g., just initialize all 4 immediately, rather than lazily (and with (presumably)
//      less-readable code)
int layer_used_indices_size[NUM_LAYERS] = { -1, -1, -1, -1 };

bool rgb_matrix_indicators_user(void) {
    uint8_t layer = biton32(layer_state);

    // when fn is held down, set lights yellow on keys whose behavior changed from base layer; set others to blue
    // inspired by https://www.reddit.com/r/olkb/comments/kpro3p/comment/h3nb56h

    switch (layer) {
        case MAC_BASE:
        case WIN_BASE:
            break;
        case MAC_FN:
        case WIN_FN:
            // just initialize layers_used_indices[layer] (at most) once per layer
            if (layer_used_indices_size[layer] == -1) {
                layer_used_indices_size[layer] = initialize_layer_used_indices(layer, layers_used_indices[layer]);
            }
//            rgb_matrix_set_color_all(255, 255, 0); // uncomment to have transparent keys appear solid blue
            for (int i = 0; i < layer_used_indices_size[layer]; ++i) {
                rgb_matrix_set_color(layers_used_indices[layer][i], 255, 255, 0);
            }
            break;
    }

    return false;
}
